package calculadora;

/**
 * Classe que implementa a operação de adição.
 */
public class Somar {

    /**
     * Realiza a operação de adição entre dois números inteiros.
     *
     * @param num1 O primeiro número a ser somado.
     * @param num2 O segundo número a ser somado.
     * @return O resultado da adição entre num1 e num2.
     */
    public int somar(int num1, int num2) {
        return num1 + num2;
    }
}
