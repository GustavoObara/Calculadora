package calculadora;

/**
 * Classe que implementa a operação de subtração.
 */
public class Subtrair {

    /**
     * Realiza a operação de subtração entre dois números inteiros.
     *
     * @param num1 O número do qual será subtraído o segundo número.
     * @param num2 O número a ser subtraído de num1.
     * @return O resultado da subtração entre num1 e num2.
     */
    public int subtrair(int num1, int num2) {
        return num1 - num2;
    }
}
