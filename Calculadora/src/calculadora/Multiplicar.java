package calculadora;

/**
 * Classe que implementa a operação de multiplicação.
 */
public class Multiplicar {

    /**
     * Realiza a operação de multiplicação entre dois números inteiros.
     *
     * @param num1 O primeiro número a ser multiplicado.
     * @param num2 O segundo número a ser multiplicado.
     * @return O resultado da multiplicação entre num1 e num2.
     */
    public int multiplicar(int num1, int num2) {
        return num1 * num2;
    }
}